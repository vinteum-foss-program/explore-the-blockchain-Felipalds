#!/bin/bash

# Using descriptors, compute the taproot address at index 100 derived from this extended public key:
#   `xpub6Cx5tvq6nACSLJdra1A6WjqTo1SgeUZRFqsX5ysEtVBMwhCCRa4kfgFqaT2o1kwL3esB1PsYr3CUdfRZYfLHJunNWUABKftK2NjHUtzDms2`

pk=xpub6Cx5tvq6nACSLJdra1A6WjqTo1SgeUZRFqsX5ysEtVBMwhCCRa4kfgFqaT2o1kwL3esB1PsYr3CUdfRZYfLHJunNWUABKftK2NjHUtzDms2
# echo "$pk"
checksum=$(bitcoin-cli getdescriptorinfo "tr($pk/100)" | jq -r '.checksum')
# echo $checksum
bitcoin-cli deriveaddresses "tr($pk/100)#$checksum" | jq -r '.[0]'
